    <div class="mt-3">
        <h6 class="font-weight-bold">CONSTITUTION OF THE</h6>
        <h6 class="font-weight-bold">ASSOCIATION OF UNAIDED COLLEGE TEACHERS</h6>
    </div>
    <div class="mt-5">
        <p class="font-weight-bold">Clause 1. NAME</p>
        <p>This association of college teachers of affiliated non-government colleges of Punjab and Chandigarh shall be
            called 'Punjab and Chandigarh College Teachers' Union, hereafter termed as Union.</p>
        <p>
            A) The term college teachers shall include all those persons such as professors, Readers, Lecturers,
            Directors/ instructors, Physical Education, Tutors, Demonstrators, and Librarians etc. who are borne on pay
            scales recommended by the U.G.C.
            <br><br>
            B) Insignia of the Union: The Union shall have its insignia/emblem/representing the aspiration of the
            teachers. The insignia will consist of the full name of the Union, its abbreviated form, motto, three
            symbols and a colour scheme as per details given in the Annexure (E).
        </p>
        <p>Name: <b>ASSOCIATION OF UNAIDED COLLEGE TEACHERS</b></p>
        <p>Abbreviated Form: <b> AUCT</b></p>
        <p class="font-weight-bold">Motto Symbols: Unite and Struggle</p>
        <p>
            a) An open book
            <br><br>
            b) A feather-pen dipped in inkpot
            <br><br>
            c) An aloft (classic style) torch with glowing flame held in right hand (fore-arm only).
        </p>
        <p><b>Colours:</b>White. Black, Blue and Red</p>
        <p><b>Clause 2. REGISTERED OFICE:</b> The registered office of the Association shall be at such a place as may
            be
            decided by the council from time to time.</p>

        <p>
            <b>Clause 3. AIMS AND OBJECTS</b>
            <br><br>
            (a) To organise all college teachers employed in affiliated non-government colleges in Punjab and Chandigarh
            to secure and maintain for them fair and adequate standard of basic salaries, allowances, reasonable working
            hours and other terms of service i.e. concessions in PF, gratuity, leave, housing accommodation, medical aid
            and pension etc. and generally to devise ways and means for improving the status and promoting welfare of
            college teachers.
            <br><br>
            (b) To promote a feeling of brotherhood and co-operation among the members and the colleges and to foster a
            spirit of service, comradeship and solidarity among the members and to develop in them a sense of
            discipline, responsibility and devotion to duty.
            <br><br>
            (c) To promote harmonious relations between the employers and the employees and to secure by all legitimate
            means, the redress of grievances of the members and to provide aid and assistance, as and when feasible to
            its members and office-bearers during trade disputes, legal proceedings, sickness and unemployment, etc.,
            arising out of their services.
            <br><br>
            (d) To educate the members of the Union in matters of their civil and political rights and duties and to get
            such legislative measures enacted and enforced, as are necessary to safe-guard the interests of the college
            teachers.
            <br><br>
            (e) To help in reorganizing the educational system in Punjab and Chandigarh on rational, democratic and
            progressive lines.
            <br><br>
            (f) To raise special funds, if and when necessary, for any or all of the aforesaid objects, as deemed
            necessary for growth, extension and up-keep of the Union.
            <br><br>
            (g) To make every Endeavour for furthering the aforesaid objects by all legitimate and democratic methods,
            such as representations, negotiations, mass meetings, demonstrations, propaganda and strikes, as and when
            decided upon.
            <br><br>
            (h) To federate and re-affiliate with any or all the Unions of teachers and other employees and colleges and
            schools in Punjab and Chandigarh or in any part of the country as well as to join hands with any other
            organization, union or society or association having kindred aims and objects on reciprocal basis.
            <br><br>
            (i) And generally to do all such things as may be considered necessary, incidental or conductive to the
            attainment of the above objects always to the limits and scope of the provisions of the Indian Trade Union
            Act, 1926 as amended up to date.
        </p>
        <p>
            <b>Clause 4. MEMBERSHIP:</b>
            <br><br>
            (a) “Any college teacher, except one appointed on adhoc basis, part time basis or against a leave vacancy,
            working in a Non-Govt. affiliated college of Punjab and Chandigarh, excluding a college set up under
            regulatory provisions of professional bodies like Medical Council of India, Dental Council of India, All
            India Council of Technical Education, National Council of Teachers Education, Bar Council of India etc.,
            with the exception of grant-in-aid B.Ed. Colleges, may become a member of the union on signing a prescribed
            form of membership (as specified in Appendix-A or Appendix-B) and taking pledge of professional good conduct
            (Appendix-C). He / She shall further be required to pay annually the Membership fee of the union to be
            determined by the General Council from time to time. In the case of failure of the payment of the membership
            fee, within a prescribed " time as determined by the General Council, he/she will cease to be a member but
            may be re-admitted on paying the Membership fee. He/ She may be further required to pay special fund/Levy as
            determined by the EC/General Council for a special purpose.
            <br><br>
            (b) The principals of the colleges, by reason of their executive positions, cannot become members of the
            Union.
            <br><br>
            (c) Notwithstanding anything in section (a) of clause 4, member of the Union, who is victimized will
            continue as a member, if he/she so desires, so long as he/she does not change his/her profession. He/ She
            will enjoy all the rights and obligations consequent upon the membership of the Union.
            <br><br>
            (d) Members of the Union in a college shall form a college unit. The minimum number required for forming a
            unit shall be half of the total strength of the college teachers. Provided that, if the Executive Committee
            is convinced that peculiar circumstances exist to justify relaxation, may allow the formation of a unit if
            ten members or 1/3rd of the total strength of the staff agree to become the members of the Union. Every unit
            shall have a constitution (Appendix D), adopted by the General Council from time to time.
            <br><br>
            (e) For purpose of electing and / or being elected by the General Council to the various bodies of the
            Union, a member must, by the date fixed by the EC, have paid his annual membership fee and special fund and
            observed all other formalities as laid down in the bye-laws by the EC from to time. Wherever, a Unit exists,
            fee/fund/levy shall be paid through the unit president or secretary. In case, the receipt of fee is refused
            by the unit president/secretary, a teacher may send it directly along with evidence, if any, of refusal for
            the consideration of General Secretary. The finally authority shall vest in the EC.
        </p>
        <p>
            <b>Clause 5. GENERAL COUNCIL,</b>
            <br><br>
            (a) The General Council shall consist of one elected delegate for each of units having strength up to three
            members, two elected delegates with members between 4 and 10. three elected delegates each of units with
            members between 11 and 30, four elected delegates of units with members between 31 and 60, five elected
            delegates each of units with members between 61 to 100 but having at least 75% membership of the total
            strength of the eligible members of the staff of the college and six elected delegates each of units with
            more than 100 members but having at least 75% membership of the total strength of the eligible members of
            the staff of a college. These representatives will continue to be the members of the General Council till
            next annual session, unless they discontinue to be the members of the unit. Outgoing members of the
            Executive Committee shall be ex-officio members of the General Council.
            <br><br>
            (b) <b>Quorum:</b> It will be 33% of the total membership of the General Council. If the Quorum is not
            present
            within one hour of a meeting's time, the members present shall constitute the quorum for the purpose of
            adopting reports presented by the General Secretary and the Finance Secretary.
            <br><br>
            (c) Representatives who become members of the Executive Committee will be replaced by the unit concerned.
            <br><br>
            (d) Tenure, summoning a General Council meeting, etc. The tenure of the General Council will be ordinarily
            two year. A meeting of the General Council can be summoned by the Executive Committee of the Union with a
            previous notice of at least 10 days. A meeting can be requisitioned by 15% members of the Council, the
            notice in this case being at least 10 days before the meeting.
            <br><br>
            (e) Bi-annual session of the general council shall be convened by 31st of July in that year. At least one
            more session of the General Council shall be convened between two election meetings of General Council.
        </p>
        <p>
            <b>Clause 6. DISTRICT COUNCIL</b>
            <br><br>
            (a) Members of the General Council from a District shall be members of the District Council of that
            District. For this purpose the Union Territory of Chandigarh shall be treated as a District.
            <br><br>
            (b) The District Council shall elect a President, a Secretary and 3 to 5 members of the Executive from among
            themselves.
            <br><br>
            (c) The Presidents of the District Councils shall be Ex-officio Members of the Executive Committee of the
            P.C.C.T.U.
            <br><br>
            (d) Members of the Executive Committee of the P.C.C.T.U. from a particular district shall be the Ex-officio
            members of the District Council and District Executive.
            <br><br>
            (e) The duties of the office-bearers of the District Council will be on the same pattern as those of the
            office-bearers of the P.C.C.T.U.
            <br><br>
            (f) Rules regarding the allocation of funds to the District Council will be framed by the Executive
            Committee of the P.C.C.T.U.
            <br><br>
            The District Council and its Executive Committee will look after the interest of the college teachers in the
            District and will deal with the matters under the guidance of the Executive Committee of the P.C.C.T.U. and
            its office-bearers.
        </p>
        <p>
            <b>Clause 7. MANAGEMENT OF THE UNION</b>
            <br><br>
            (a) (i) the EC of the Union shall consist of a President, a Vice-President, a General Secretary, three
            Secretaries (one from each University area), a Finance Secretary, and 11 others all to be elected by the
            General Council from among the members of the Union, (ii) Outgoing President and General Secretary, (iii)
            The Convener of the Women Wing nominated by the EC. (iv) Presidents of all the District Councils, (v) Any
            member who is office bearer of ALL INDIA FEDERATION or STATE FEDERATION OF COLLEGE/UNIVERSITY TEACHERS to
            which the Union is affiliated and university area-wise co-conveners of women wing, nominated by the EC, will
            attend the meetings as special invitees.
            <br><br>
            (ii) The following shall be conditions of eligibility for election to the various offices of the Union :—
            <br><br>
            <b>President:</b> At least one term as members of the Executive Committee or four years continuous
            membership of
            the Union. A break occurring due to study leave/ medical leave/ex-India leave is to be ignored.
            <br><br>
            <b>General Secretary:</b> Same as above.
        </p>
        <p>
            <b>Other offices including members of the Executive Committee.</b>
            <br><br>
            (a) At least two years continuous membership of the Union.* A break occurring due to study leave/ medical
            leave/ex-India leave is to be ignored.
            <br><br>
            (b) The Executive Committee including the office-bearers shall not have more than one person from the same
            Unit on it. This shall not apply to Ex-officio members of the Executive Committee.
            <br><br>
            (c) No member of the Union can seek election to any office including membership of the Executive Committee
            unless he/she has cleared all his/her account due to the Union.
            <br><br>
            (d) No member of the Union shall be elected for more than two consecutive terms to any office**.
            <br><br>
            (e) For the various offices and membership of the Executive Committee nominations and proposals may be made
            by any two members of the Union.
            <br><br>
            (f) All nominations shall reach the General Secretary at least 21 days before the General Council meeting
            convened to elect office-bearers and other members of the Executive Committee.
            <br><br>
            (g) The General Secretary shall announce all the proposals for nominations, duly received, at least 14 days
            before the General Council meets.
            <br><br>
            (h) In case nominations of two different persons belonging to the same unit are received for two offices of
            the Union, both the persons proposed will have the right to contest the election.
            <br><br>
            (i) In case, two or more persons belonging to the same unit are elected to two or more offices of the Union,
            the one who secures the highest number of votes or is elected to a higher office will retain the seat. The
            order of office shall be President, General Secretary, Vice-President, Secretary, Finance Secretary and
            member of the Executive Committee.
            <br><br>
            (j) If no nominations are received for one or more offices of the Union the elections may be effected on the
            floor of the House.
            <br><br>
            (k) The election of the Executive Committee other than the Ex-officio members shall take place in the
            bi-annual session of the General Council.
            <br><br>
            (l) The Executive Committee shall, from time to time, frame bye-laws for the efficient functioning of the
            Union.
            <br><br>
            (m) The preliminary list of members of General Council shall be made available by April 30th of the year of
            election and the final list shall be made available by May 15th of the year of election.
        </p>
        <p>
            <b>Clause 8. MEETING OF THE EXECUTIVE COMMITTEE</b>
            <br><br>
            (a) The Executive Committee of the Union shall meet at least once in two months.
            <br><br>
            (b) In case of the ordinary meetings of the Executive Committee, the President and the General Secretary
            shall decide about the date, time and the place of the meeting provided that not more than three consecutive
            meetings of the Executive Committee are held in the same town.
            <br><br>
            (c) A meeting of the Executive Committee can be requisitioned on receipt of the duly signed requisition by
            at least five members of the Executive Committee. The General Secretary shall convene such a meeting within
            a fortnight on receipt of such requisition.
            <br><br>
            (d) The quorum for the EC meeting shall be 1/3 of its strength.
            <br><br>
            (e) The office-bearers shall be competent to meet and take decision in between the two meetings of the EC.
            However, all decisions taken by the office bearers shall be placed before the EC for its approval and
            ratification.
        </p>
        <p>
            <b>Clause 9. LIST OF MEMBERS</b>
            <br><br>
            The Union shall maintain at its registered office a register of the members of the Union with their names
            and addresses in full. The register shall be a complete record of the members of the Union, and shall be
            open for inspection by any member of the Union at a 15 days' notice.
        </p>
        <p>
            <b>Clause 10. VACANCIES OF OFFICE</b>
            <br><br>
            (a) If a member of the Executive Committee does not attend three consecutive meetings without sufficient
            reason acceptable to the Executive Committee, or ceases to be a member of the Union, or if a person resigns
            and his resignation is accepted by the Executive Committee, he shall cease to be a member of the Executive
            Committee.
            <br><br>
            (b) The Executive Committee shall fill up such vacancies in the following manner.
        </p>
        <ol>
            <li>In case of the President, the Vice-President shall ipso-facto became the President till a new President
                is elected by the General Council.
            </li>
            <li>
                In case of General Secretary, one of the Secretaries shall ipso-facto become General Secretary till a
                new General Secretary is elected by the General Council.
            </li>
            <li>
                In case of the Vice-President, Secretary and Finance Secretary, the members of the Executive Committee,
                shall appoint any member from among themselves to the offices; and in case of the members of the
                Executive Committee, the Executive Committee shall have the right to co-opt any member of the Union to
                the Executive Committee by its majority decision. Not more than two co-options shall, however, be made
                in this manner in one session.
            </li>
        </ol>
        <p>
            <b>Clause 11. FUNCTIONS OF THE OFFICE-BEARERS</b>
            <br><br>
            (a) The President shall supervise and control all the activities of the Union, preside over the General
            Council and other meetings of the Union, execute and conduct their proceedings and with the assistance of a
            Committee consisting of the Vice-President and the General Secretary, deal with the cases of victimization,
            grievances and complaints of the members.
            <br><br>
            (b) The Vice-President shall assist and help him in the performance of his official duties and shall
            exercise full rights and privileges of the President in the case of his absence, resignation, death or
            removal till the new President is elected.
            <br><br>
            (c) The General Secretary shall have to do all the executive and office work of the Union, supervise and
            coordinate the work of the District Councils, countersign all bills and receipts.
            <br><br>
            (d) Each Secretary shall look after the affairs relating to his/her university under the over-all guidance
            and control of the EC. They shall also help and assist the General Secretary in performance of his official
            duties. In case of absence, resignation, death or removal of the General Secretary, the EC shall promote one
            of the Secretaries as General Secretary and he shall exercise full right and privileges of the General
            Secretary till the new General Secretary is elected.
            <br><br>
            (e) The Finance Secretary shall be in charge of all monetary matters involving receipts, payments, and
            maintenance of proper books of accounts of the Union. He shall prepare budget estimates for the ensuing year
            under the guidance of the General Secretary and the Accounts Committee each year and, after obtaining the
            President's consent and place it for sanction before the Executive Committee and the General Council.
            <br><br>
            (f) Every member of the Executive Committee shall be allotted a certain number of Colleges by the Executive
            Committee at the beginning of the new term of office and he shall be responsible to the Executive Committee
            and General Council for:
        </p>
        <ol>
            <li>Organization of new units in these colleges:</li>
            <li>Maintenance, inspection and guidance of existing units:</li>
            <li>Attending by correspondence and through personal visits, to all cases of victims and individual
                complaints of members of the Union in these colleges.
            </li>
        </ol>
        <p>
            <b>Clause 12. DISCIPLINARY ACTION</b>
            <br><br>
            (a) The Executive Committee of the Union shall have the right to expel or suspend any District Council,
            District Executive, Unit or member of the Union for breach of discipline or act prejudicial to the interests
            of the Union. The affected members of bodies shall have the right to appeal against the decision of the
            Executive Committee to the General Council, the decision of which shall be final. Any such action shall not
            be taken by the Executive Committee without serving a due notice to the member of body concerned, for
            rendering his/her or its explanation in writing or in person before the Committee.
            <br><br>
            (b) The Union shall have no right to impose any fines or penalty for any offences committed by any member of
            the Union, except as provided in Clause No. 12(a) above.
        </p>
        <p>
            <b>Clause 13. FUNDS AND THEIR UTILIZATION</b>
            <br><br>
            (a) The funds of the Union shall consist of receipts from admission fee, subscription, special levies,
            contributions and donations received from the members of the Union or any other person directly or
            indirectly in sympathy with the cause and objectives of the Union.
            <br><br>
            (b) The funds of the Union shall be maintained in a bank in the name of:-. “Union” or under the head Finance
            Secretary, PCCTU. The account shall be opened and operated by the Finance Secretary. The surplus funds of
            the union may be invested in better revenue earning schemes, with the approval of the EC.
            <br><br>
            (c) Any member of the union may inspect the accounts on 15 days' notice to the General Secretary, as per
            provisions and procedure in the bye-laws.
            <br><br>
            (d) The Finance Secretary of the Union shall arrange to put the accounts of the Union, duly audited, at the
            annual session of the General Council.
            <br><br>
            (e) The funds of the Union will be utilized for furthering the aims and objectives of the Union as laid down
            in clause 3 and for running the day-to-day work of the Union and to provide financial help to a member of
            the Union in case of dismissal, retrenchment or sickness etc. to the extent the finances of the Union
            permit. The grant and extent of such help shall be determined by the Executive Committee by its 2/3rd
            majority decision.
            <br><br>
            (f) The accounts of the Union shall be audited by an Audit Committee, provided that no member of the
            Executive Committee can be nominated to the Audit Committee. The accounts, thus audited, will be presented
            before the Genera! Council for confirmation.
        </p>
        <p>
            <b>Clause 14. POWER TO FRAME RULES AND REGULATIONS</b>
            <br><br>
            The Executive Committee shall have the power to frame rules and regulations, subject to the provisions of
            the Constitution from time to time.
        </p>
        <p>
            <b>Clause 15. RIGHTS OF THE MEMBERS</b>
            <br><br>
            A member of the Union shall be entitled to the rights and privileges provided in the constitution, so long
            as he/she observes the rules and regulations of the Union.
        </p>
        <p>
            <b>Clause 16. VOTE OF NO-CONFIDENCE</b>
            <br><br>
            A vote of no-confidence can be moved against any member of the Executive Committee on the receipt of a
            signed requisition from at least 1/4th of the total membership of the Union. The General Secretary shall
            convene a special meeting of the General Council on the receipt of such requisition and the vote of
            no-confidence shall be carried by a 2/3 majority of the General Council present and voting, subject to
            clause 5 d.
        </p>
        <p>
            <b>Clause 17. AMENDMENT</b>
            <br><br>
            Any Amendment to the Constitution shall be proposed by at least one tenth members of the Union or by any
            single unit or by Executive Committee, and it shall be deemed to have become a part of the Constitution, as
            and when ratified by a 3/4th majority of the General Council present and voting.
        </p>
        <p>
            <b>Clause 18. STRIKE</b>
            <br><br>
            The General Council shall have the right to declare a general strike with its decision by 2/3 rd majority by
            giving the information to the employers fifteen days in advance.
        </p>
        <p>
            <b>Clause 19. DISSOLUTION</b>
            <br><br>
            The Union can be dissolved in an extra-ordinary meeting of the General Council, convened for the purpose by
            a 3/4th majority vote of the total membership of the General Council. One month's prior notice is essential
            for convening such a meeting, which shall be required to be signed by at least 1/3rd of the total members of
            the union.
        </p>
